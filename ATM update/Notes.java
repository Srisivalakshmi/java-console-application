public class Notes {
    private int note;
    private int count;

    public void Notes(int note, int count) {
        this.note = note;
        this.count = count;
    }
    public int getNote() {
        return note;
    }
    public int setNote(int note){
        return note;
    }
    public int getCount(){
        return count;
    }
    public int setCount(int count){
        return count;
    }

}
